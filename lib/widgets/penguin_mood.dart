/// يمثل الحالات المزاجية والتفاعلية لشخصية البطريق في التطبيق
enum PenguinMood {
  /// الحالة الافتراضية أو الهادئة
  normal,

  /// عند تحقيق إنجاز أو إنهاء مستوى بنجاح
  success,

  /// لإظهار السعادة والترحيب (مثلاً في الملف الشخصي)
  happy,

  /// عند إعطاء تعليمات أو نطق الحروف
  speaking,

  /// لتحفيز المستخدم في السلسلة اليومية أو التحديات
  encouraging,

  /// في حال الإخفاق في تمرين أو الخطأ في النطق
  sad,

  /// حالة إضافية: تفكير أو حيرة، يمكن استخدامها للتلميحات أو انتظار المستخدم
  thinking,
}